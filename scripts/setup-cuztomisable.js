// scripts/setup-cuztomisable.js

const fs = require('fs');
const path = require('path');

// =========================
// Find project root
// =========================
function findProjectRoot(startDir) {
    let dir = startDir;
    while (dir !== path.parse(dir).root) {
        const pkgPath = path.join(dir, 'package.json');
        if (fs.existsSync(pkgPath)) {
            try {
                const pkg = JSON.parse(fs.readFileSync(pkgPath, 'utf8'));
                if (pkg.name !== '@vandmade/cuztomisable') {
                    return dir;
                }
            } catch {}
        }
        dir = path.dirname(dir);
    }
    return startDir;
}

const projectRoot = findProjectRoot(process.cwd());

const PACKAGE_APP_DIR = path.join(__dirname, '../src/app');
const PACKAGE_ASSETS_DIR = path.join(__dirname, '../assets');

const APP_DIR = path.join(projectRoot, 'app');
const APP_ASSETS_DIR = path.join(projectRoot, 'assets');

// =========================
// Copy assets (only if missing)
// =========================
function copyAssetsIfMissing(srcDir, destDir) {
    if (!fs.existsSync(srcDir)) return;

    if (!fs.existsSync(destDir)) {
        fs.mkdirSync(destDir, { recursive: true });
    }

    fs.readdirSync(srcDir, { withFileTypes: true }).forEach((entry) => {
        const srcPath = path.join(srcDir, entry.name);
        const destPath = path.join(destDir, entry.name);

        if (entry.isDirectory()) {
            copyAssetsIfMissing(srcPath, destPath);
        } else if (entry.isFile()) {
            if (!fs.existsSync(destPath)) {
                fs.copyFileSync(srcPath, destPath);
                console.log(`Copied asset: ${destPath}`);
            }
        }
    });
}

copyAssetsIfMissing(PACKAGE_ASSETS_DIR, APP_ASSETS_DIR);

// =========================
// Generate app/_layout.tsx (ONLY if missing)
// =========================
const APP_LAYOUT_FILE = path.join(APP_DIR, '_layout.tsx');

const layoutTemplate = `// Auto-generated by cuztomisable
import {
    AppProvider,
    AuthProvider,
    MessageProvider,
    ThemeWrapper,
} from '@vandmade/cuztomisable';
import { Message } from '@vandmade/cuztomisable/components/ui/Message';
import { Stack } from 'expo-router';

export default function RootLayout() {
    return (
        <AppProvider config={{}}>
            <ThemeWrapper>
                <MessageProvider>
                    <AuthProvider>
                        <Message />
                        <Stack 
                            screenOptions={{
                                headerShown: false,
                            }} />
                    </AuthProvider>
                </MessageProvider>
            </ThemeWrapper>
        </AppProvider>
    );
}
`;

if (!fs.existsSync(APP_LAYOUT_FILE)) {
    if (!fs.existsSync(APP_DIR)) {
        fs.mkdirSync(APP_DIR, { recursive: true });
    }

    fs.writeFileSync(APP_LAYOUT_FILE, layoutTemplate, 'utf8');
    console.log('Created app/_layout.tsx');
}

// =========================
// Helpers
// =========================
function walk(dir, callback, relativePath = '') {
    fs.readdirSync(dir, { withFileTypes: true }).forEach((entry) => {
        const fullPath = path.join(dir, entry.name);
        const relPath = path.join(relativePath, entry.name);

        if (entry.isDirectory()) {
            walk(fullPath, callback, relPath);
        } else if (entry.isFile() && entry.name.endsWith('.tsx')) {
            callback(fullPath, relPath);
        }
    });
}

// =========================
// Main
// =========================
function main() {
    if (projectRoot === path.resolve(__dirname, '..', '..')) {
        console.log('Skipping generation inside package');
        return;
    }

    // Generate route re-exports
    walk(PACKAGE_APP_DIR, (srcFile, relPath) => {
        const normalizedRelPath = relPath.replace(/\\/g, '/');

        // 🚨 Skip root layout (handled separately)
        if (normalizedRelPath === '_layout.tsx') return;

        // 🚨 Skip internal-only routes
        if (normalizedRelPath.split('/').includes('(cuztomisable)')) return;

        const destFile = path.join(APP_DIR, relPath);
        const destDir = path.dirname(destFile);

        if (!fs.existsSync(destDir)) {
            fs.mkdirSync(destDir, { recursive: true });
        }

        // Build import path → dist/app/...
        const importPath = `@vandmade/cuztomisable/app/${normalizedRelPath.replace('.tsx', '')}`;

        const content = `// Auto-generated by cuztomisable
// You can safely delete or override this file.

export { default } from "${importPath}";
`;

        // ✅ DO NOT overwrite existing files
        if (!fs.existsSync(destFile)) {
            fs.writeFileSync(destFile, content, 'utf8');
            console.log(`Generated: ${destFile}`);
        }
    });

    // Ensure root layout exists
    if (!fs.existsSync(APP_LAYOUT_FILE)) {
        if (!fs.existsSync(APP_DIR)) {
            fs.mkdirSync(APP_DIR, { recursive: true });
        }

        fs.writeFileSync(APP_LAYOUT_FILE, layoutTemplate, 'utf8');
        console.log('Created app/_layout.tsx');
    }
}

main();